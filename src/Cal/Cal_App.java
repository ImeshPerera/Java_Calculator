    private void percentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_percentBtnActionPerformed
        try {
            // If expression contains operator
            if (expression.contains("/")) {

                String[] parts = expression.split("/");

                double first = Double.parseDouble(parts[0]);
                double second = Double.parseDouble(parts[1]);

                double result = (first / second) * 100;

                expression = String.format("%.2f", result);
                displayTf.setText(expression + " %");

            } else {
                // fallback normal percentage
                double value = Double.parseDouble(displayTf.getText());
                value = value / 100;
                expression = formatResult(value);
                displayTf.setText(expression);
            }

        } catch (Exception e) {
            displayTf.setText("Error");
            expression = "";
        }
    }//GEN-LAST:event_percentBtnActionPerformed

