    private double evaluateExpression(String expr) {

        java.util.Stack<Double> numbers = new java.util.Stack<>();
        java.util.Stack<Character> operators = new java.util.Stack<>();

        for (int i = 0; i < expr.length(); i++) {

            char ch = expr.charAt(i);

            // If digit or dot → build number
            if (Character.isDigit(ch) || ch == '.') {
                StringBuilder sb = new StringBuilder();

                while (i < expr.length()
                        && (Character.isDigit(expr.charAt(i)) || expr.charAt(i) == '.')) {

                    sb.append(expr.charAt(i));
                    i++;
                }
                i--;

                numbers.push(Double.parseDouble(sb.toString()));
            } // If operator
            else if (ch == '+' || ch == '-' || ch == '*' || ch == '/') {

                while (!operators.isEmpty()
                        && precedence(operators.peek()) >= precedence(ch)) {

                    double b = numbers.pop();
                    double a = numbers.pop();
                    char op = operators.pop();

                    numbers.push(applyOperation(a, b, op));
                }

                operators.push(ch);
            }
        }

        // Remaining operations
        while (!operators.isEmpty()) {
            double b = numbers.pop();
            double a = numbers.pop();
            char op = operators.pop();

            numbers.push(applyOperation(a, b, op));
        }

        return numbers.pop();
    }

    private int precedence(char op) {
        if (op == '+' || op == '-') {
            return 1;
        }
        if (op == '*' || op == '/') {
            return 2;
        }
        return 0;
    }

    private double applyOperation(double a, double b, char op) {

        switch (op) {
            case '+':
                return a + b;
            case '-':
                return a - b;
            case '*':
                return a * b;
            case '/':
                if (b == 0) {
                    throw new ArithmeticException("Divide by zero");
                }
                return a / b;
        }

        return 0;
    }

    private String formatResult(double value) {

        // If value is very large or very small → scientific notation
        if (Math.abs(value) >= 1e14 || (Math.abs(value) > 0 && Math.abs(value) < 1e-13)) {

            java.text.DecimalFormat sci = new java.text.DecimalFormat("0.############E0");
            sci.setMaximumFractionDigits(8); // adjust precision if needed
            return sci.format(value);
        }

        // Normal formatting
        java.text.DecimalFormat normal = new java.text.DecimalFormat("0.##############");
        normal.setMaximumFractionDigits(12);
        return normal.format(value);
    }

