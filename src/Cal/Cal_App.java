    private void sqrtBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sqrtBtnActionPerformed
        try {
            double value = Double.parseDouble(displayTf.getText());

            if (value < 0) {
                displayTf.setText("Error");
                expression = "";
                return;
            }

            value = Math.sqrt(value);
            expression = formatResult(value);
            displayTf.setText(expression);
        } catch (Exception e) {
            displayTf.setText("Error");
            expression = "";
        }
    }//GEN-LAST:event_sqrtBtnActionPerformed

